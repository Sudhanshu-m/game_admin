// Get classes grouped by year
export const getClassesByYear = () => {
  const grouped = {
    'BDS 1st Year': [],
    'BDS 2nd Year': [],
    'BDS 3rd Year': [],
    'BDS 4th Year': [],
    'BDS 5th Year': [],
    'MDS 1st Year': [],
    'MDS 2nd Year': [],
    'MDS 3rd Year': []
  };

  dentalClasses.forEach(cls => {
    if (grouped[cls.grade]) {
      grouped[cls.grade].push(cls);
    }
  });

  return grouped;
};

